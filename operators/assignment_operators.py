import os

n1 = 4
n2 = 2

os.system('cls')

# Entendo os operadores de atribuição
print(f'Considerando que n1 vale {n1} e n2 vale {n2}, então:')
n1 = n2
print('Se n1 = n2, então n1 agora vale', n1)
n1 += 10 # n1 = n1 + 10
print('Se n1 += 10, então n1 agora vale', n1)
n1 -= 5 # n1 = n1 - 5
print('Se n1 -= 5, então n1 agora vale', n1)
